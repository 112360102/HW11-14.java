public class Sample3_18
{
    public static void main(string[] args)
    {
        string str = "Hello";

        char ch1 = str.charAt(0);
        char ch2 = str.charAt(1);

        int len = str.length();

        system.out.println(str + "的第一個字元是" + ch1);
        system.out.println(str + "的第二個字元是" + ch2);
        system.out.println(str + "的長度是" + len);
    }
}